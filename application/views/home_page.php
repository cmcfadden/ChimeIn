
<h1><span class="headingwhite">ChimeIn</span></h1>

<div id='course_portal'><a href="<?=str_replace("http:", "https:", site_url())?>course/">Log In</a></div>

<div>
<p>ChimeIn is a powerful and fun web-based student response tool.  No need to manage dozens or hundreds of expensive clickers - ChimeIn leverages the digital devices students already carry to provide a rich user experience.  Instructors manage questions via a simple web interface, and students can respond via web browsers, smart phones, or text messages.  </p>

<p>We’ve created a <a href="http://mediamill.cla.umn.edu/mediamill2/html5/78553">short video</a> to provide a ChimeIn overview.  Our “<a href="https://sites.google.com/a/umn.edu/cla-support/applications/chimein">Help</a>” also includes a series of introductory documents.</p>

<p>If you’re staff or faculty at the University of Minnesota, you should already have access.  Click “Log In” above to get started.  If you’re a student enrolled in a course that uses ChimeIn, click “Log In” and then select your course.  We're in beta at the moment, so you may occasionally run into errors, odd messages, or periods of unavailability.</p>

<p>We strongly recommend using a modern webbrowser when using ChimeIn.  These include Safari 4+, Chrome 5+, FireFox 3.6+, or Internet Explorer 8+.</p>
<p>ChimeIn grew out of <a href="http://www.hist.umn.edu/people/profile.php?UID=reyer001">Dr. Kay Reyerson</a>'s "Twitter project" for her CLA Course Transformation grant in spring 2010.</p>
<p>If you have any questions, please <a href="mailto:4help@umn.edu">let us know</a>.</p>
